public class DifTwoNumbers {
    public static void main(String[] args) {
        double num1 = 10; // First number
        double num2 = 20; // Second number
        double dif = num1 - num2; // Calculate the sum

        System.out.println("The difference of " + num1 + " and " + num2 + " is: " + dif);
    }
}


